public class InitialisePrimitivite {
	public static void main(String[] args) {
		int a = 5;
		int b=6,c=97;
		int d;
		d=9;
		
		
		byte aa =(byte)a;
		System.out.println(aa);
		short bb =(short)b;
		System.out.println(bb);
		char cc=(char) c;
		System.out.println(cc);
		long dd = d;
		System.out.println(dd);
		float ee =d;
		System.out.println(ee);
		double ff =d;
		System.out.println(ff);
		
		
		
		
		
	}
}
