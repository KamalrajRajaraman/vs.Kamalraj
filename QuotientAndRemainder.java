package BasicOpenLabBook;

public class QuotientAndRemainder {
	public static void main(String[] args) {
		int dividend = 24;
		int divisor = 4;
		
		int quoitent = dividend/divisor;
		System.out.println("Quotient of dividing "+dividend+" and "+divisor+" is "+quoitent);
		
		int remainder = dividend % divisor;
		System.out.println("Remainder of dividing "+dividend+" and "+divisor+" is "+remainder);
		
		
	}
}
